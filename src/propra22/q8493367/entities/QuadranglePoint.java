package propra22.q8493367.entities;



/**
 * The Enum QuadranglePoint is used to identify one of the four points of a 
 * quadrangle. These four points are called A, B, C and D. The points A and C
 * represent the antipodal pair. See also the specification at page 23 
 * (Abbildung 2.4).
 */
public enum QuadranglePoint {
     
     /** The point A of the quadrangle */
     A, 
	 /** The point B of the quadrangle */
	 B, 
	 /** The point C of the quadrangle */
	 C, 
	 /** The point D of the quadrangle */
	 D;
}
